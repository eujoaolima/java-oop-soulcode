package Aula09;

public class Heranca {
    public static void main(String[] args) {
        // Heranca
    
        /*
        
        Heranca e uma forma de herdarmos atributos e metodos de outros atributos e metodos sem precisarmos criarmos novos objetos.

         * Conceitos gerais:
         * - Reutilizacao de codigo;
         * - Herdar metodos e atributos de classes pai;
         * - Criar classes filhas;
         * - Otimizacao de codigo.

         * Nos temos alguns tipos de heranca, como:
        
         * - Associacao: Quando uma classe tem atributos e metodos de outra classe;
        
         * - Agregacao: Quando varias classes participam da classe principal porem a classe principal nao tem em seu escopo essas classes;
        
         * - Composicao: Quando uma classe representa todas as outras classes e podemos aciona-la em diversas partes.
         */

        Funcionario f = new Funcionario("Batata", "12345679810", 10);

        System.out.println("Nome do funcionario: " + f.getNome());
        System.out.println("CPF: " + f.getCpf());
    }
}
